#  Make a list of the numbers from one to one hundred, and then use min() and max()
#  to make sure your list actually starts at one and ends at one hundred.
# Also, use the sum() function to see how quickly Python can add a million numbers.

# Making a list with one hundred numbers
one_hundred = list(range(0, 101))

print(min(one_hundred))  # Printing the first number or min value

print(max(one_hundred))  # Printing the last value of the list

print(sum(one_hundred))  # Print the sum of the list



